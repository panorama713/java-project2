package com.example.javaproject2.week1.day4;

public class MultiplicationTable {
    public static void main(String[] args) {
        // 2 * 1 = 2
        // 2 * 2 = 4
        System.out.println("2" + " * " + "1" + " = " + "2");
        System.out.printf("%d * %d = %d\n", 2, 2, 4);
        System.out.printf("%d * %d = %d\n", 2, 4, 2 * 4);
    }
}
