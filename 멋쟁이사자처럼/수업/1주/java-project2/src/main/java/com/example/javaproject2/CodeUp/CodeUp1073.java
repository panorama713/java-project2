package com.example.javaproject2.CodeUp;

import java.util.Scanner;

public class CodeUp1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int inputNum = -1;
        while (inputNum != 0) {
            inputNum = sc.nextInt();
            if (inputNum != 0) {
                System.out.println(inputNum);
            }
        }
    }
}
