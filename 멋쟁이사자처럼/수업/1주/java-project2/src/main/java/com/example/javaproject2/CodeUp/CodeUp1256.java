package com.example.javaproject2.CodeUp;

import java.util.Scanner;

public class CodeUp1256 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int cont = sc.nextInt();
        for (int i = 0; i < cont; i++) {
            System.out.printf("*");
        }
        System.out.println();
    }
}
