package com.example.javaproject2.CodeUp;

import java.util.Scanner;

public class CodeUp1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            int num = sc.nextInt();
            if (num != 0) {
                System.out.println(num);
            } else {
                break;
            }
        }
    }
}
